namespace SWP\Bundle\ContentListBundle\Form\Type;

use SWP\Component\ContentList\Model\ContentListInterface;
use Symfony\Component\Form\AbstractType;
use Symfony\Component\Form\Extension\Core\Type\ChoiceType;
use Symfony\Component\OptionsResolver\OptionsResolver;

class ContentListTypeSelectorType extends AbstractType
{
    /**
     * Default content list type choices.
     *
     * @var string[]
     */
    protected $choices = [
        'Automatic' => ContentListInterface::TYPE_AUTOMATIC,
        'Manual' => ContentListInterface::TYPE_MANUAL,
    ];

    /**
     * {@inheritdoc}
     */
    public function configureOptions(OptionsResolver $resolver)
    {
        $resolver
            ->setDefaults([
                'choices' => $this->choices,
            ])
        ;
    }

    /**
     * {@inheritdoc}
     */
    public function getParent()
    {
        return ChoiceType::class;
    }

    /**
     * {@inheritdoc}
     */
    public function getBlockPrefix()
    {
        return 'content_list_selector';
    }
}
